    printf("Crypto encrypt! Stat=%d\r\n",status);

    if(memcmp(block_cfb_256_oe,block_cfb_256,81)!=0){
        printf("Test encryption failed\r\n");
    }else{
        printf("Test encryption OK\r\n");
    }

    printf("Testing speed for kalina-256/256-CFB\r\n");
    current_time=time(NULL);
    for(i=0;i<1048576UL*5UL;i++){
        crypto_encrypt(crypto,block_cfb_256_oe,block_cfb_256_o,80,false);
    }
    finish_time=time(NULL);
