#define LIB_CRYPTO_FNAME "./libcrypto.so"

void load_crypto_lib(void **lib){

    *lib=dlopen(LIB_CRYPTO_FNAME, RTLD_NOW|RTLD_GLOBAL );
    if(!*lib){
        fprintf(stderr, "%s\n", dlerror());
        return;
    }

    crypto_init      =   dlsym(*lib, CRYPTO_INIT_FUNC );
    crypto_end       =   dlsym(*lib, CRYPTO_FREE_FUNC );
    crypto_encrypt   =   dlsym(*lib, CRYPTO_ENCRYPT_FUNC );
    crypto_decrypt   =   dlsym(*lib, CRYPTO_DECRYPT_FUNC );
    crypto_set_iv    =   dlsym(*lib, CRYPTO_SET_IV_FUNC );
    crypto_set_key   =   dlsym(*lib, CRYPTO_SET_KEY_FUNC );
}

void unload_crypto_lib(void *lib){
   dlclose( lib  );
}


